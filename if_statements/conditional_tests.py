# Boolean expressions aka conditional tests

car = 'subaru'
print("Is car == 'subaru'? I predict True.")
print(car == 'subaru')  # True

print("\nIs car == 'audi'? I predict False.")
print(car == 'audi\n')  # False 

dog = 'golden retriever'
print("Is the dog a 'golden retriever'? I predict True.")
print(dog == 'golden retriever')  # True

print("\nIs the dog a 'poodle'? I predict False.")
print(dog == 'poodle\n')  # False
